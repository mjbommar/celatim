"""Wire-test battery, graded to the test-evidence standard.

For every located+buildable mechanism, on a real Linux kernel network stack (container
netns + veth):
  - COVERT run: inject a packet with covert bits in the field, recover at the receiver tap.
  - BENIGN CONTROL: inject the SAME packets with the field forced to its benign default (0)
    and confirm the recovery yields NOTHING. This proves the recovery reads the real field
    (not an echo of our injection) and that crafting the packet is not itself the cause.
A mechanism passes only if COVERT recovers the payload AND the control recovers b"".

GRADING (stated, not implied):
  Substrate : real Linux kernel network stack in a namespace (veth, netfilter, real RX) --
              VM-tier; a real kernel, not a reimplementation.
  Synthesis : Level 2 -- the packet *entry* is synthesized (crafted + AF_PACKET injected,
              not generated by a live application); wire transit and the on-wire field are
              real; recovery reads real on-wire bytes. Disclosed here as Level 2.
Nothing is piped to /dev/null; the process exit code reflects pass/fail.

Usage: python run_battery.py [payload]
"""

from __future__ import annotations

import subprocess
import sys
import time

import lab

from celatim.catalog import load_mechanisms
from celatim.channel.framer import Framer
from celatim.channel.registry import codec_for

BUILDABLE = {
    "IPv4",
    "TCP",
    "ICMP",
    "UDP",
    "IPv6",
    "ICMPv6",
    "SCTP",
    "AH",
    "IGMP",
    "RIP",
    "OSPF",
    "DHCP",
    "NTP",
}
OUT = "/tmp/battery_out"


def mac(ns: str, dev: str) -> str:
    out = subprocess.check_output(["ip", "-n", ns, "link", "show", dev]).decode()
    return next(line.split()[1] for line in out.splitlines() if "link/ether" in line)


def run_once(m, payload: bytes, force_zero: bool, s: str, d: str) -> bytes:
    """One inject+capture; force_zero sends the field at its benign default (the control)."""
    n = len(Framer(codec_for(m)).encode(payload))
    if n == 0:
        return b""
    cap = subprocess.Popen(
        ["ip", "netns", "exec", "rcv", "python3", "lab.py", "capture", m.id, "vr", str(n), OUT]
    )
    try:
        time.sleep(1.8)
        args = [
            "ip",
            "netns",
            "exec",
            "snd",
            "python3",
            "lab.py",
            "inject",
            m.id,
            payload.hex(),
            s,
            d,
        ]
        if force_zero:
            args.append("--zero")
        subprocess.run(args, check=True)
        cap.wait(timeout=15)
        with open(OUT, "rb") as fh:
            return fh.read()
    finally:
        # never orphan the sniffer; a leaked capture interferes with later mechanisms
        if cap.poll() is None:
            cap.kill()
            cap.wait()


def main() -> None:
    payload = (sys.argv[1] if len(sys.argv) > 1 else "wire-test").encode()
    located = [m for m in load_mechanisms(lab.CATALOG) if m.locator is not None]

    lab.topo_down()
    lab.topo_up()
    s, d = mac("snd", "vs"), mac("rcv", "vr")
    rows = {}
    try:
        for m in located:
            try:
                covert = run_once(m, payload, False, s, d)
                control = run_once(m, payload, True, s, d)  # field forced to default 0
                rows[m.id] = (
                    "PASS" if (covert == payload and control == b"") else "FAIL",
                    covert,
                    control,
                )
            except subprocess.CalledProcessError:
                rows[m.id] = ("UNSUPPORTED", b"", b"")  # no packet template / build error
    finally:
        lab.topo_down()

    npass = sum(1 for v, *_ in rows.values() if v == "PASS")
    nsupp = sum(1 for v, *_ in rows.values() if v != "UNSUPPORTED")
    print(
        f"=== WIRE BATTERY (real kernel stack, Level-2 synthesis): "
        f"{npass}/{nsupp} buildable passed [covert recovers payload AND control recovers nothing]; "
        f"{len(located) - nsupp} unsupported (need a template) ==="
    )
    for mid, (v, covert, control) in rows.items():
        detail = ""
        if v == "FAIL":
            detail = f"  covert={'ok' if covert == payload else 'NO'} control={'ok' if control == b'' else 'LEAK'}"
        print(f"  {v:12} {mid}{detail}")
    sys.exit(0 if npass == nsupp else 1)


if __name__ == "__main__":
    main()
